public class Calculadora {
        public static int suma(int a, int b) {
            return a + b;
        }

        public static int resta(int b, int a) {
            return b - a;
        }

        public static int multiplica(int b, int a) {
            return a * b;
        }

        public static int divide(int b, int a) {
            return b / a;
        }
}

